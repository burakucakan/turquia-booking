		public static Hashtable GetAllLanguages() {
			SqlDataReader dr = GetAllLanguagesReader();
			return ConvertReaderToHashTable(dr);
		}
		
		/// <summary>
		/// Creates Languages for records In the prefilled DataReader, And puts them into a HashTable
		/// </summary>
		/// <param name="dr">The DataReader prefilled With the Languages records</param>
		/// <returns>The Hashtable containing Languages objects And their ID As key.</returns>
		private static Hashtable ConvertReaderToHashTable(SqlDataReader dr) {
			Hashtable result = new Hashtable();
			while (dr.Read()) {
	            Languages myLanguages = new Languages();
            
            myLanguages.m_intLanguageID = dr.GetSqlInt32(0);
            myLanguages.m_strLanguage = dr.GetSqlString(1);
            myLanguages.m_bolisActive = dr.GetSqlBoolean(2);
            
            result.Add(myLanguages.LanguageID, myLanguages);
		}
	
			return result;
		}
		
		
